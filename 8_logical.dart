void main() {
  var n1 = 19, n2 = 22;
//LOGICAL AND
  if (n1 >= 15 && n2 <= 20) {
    print("True");
  } else {
    print("False");
  }

//LOGICAL OR
  if (n1 >= 15 || n2 <= 20) {
    print("True");
  } else {
    print("False");
  }

//LOGICAL NOT
  if (!(n1 == 15)) {
    print("Its not a 15.");
  } else {
    print("Its 15.");
  }
}

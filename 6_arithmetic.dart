void main() {
  var n1 = 19, n2 = 22, sum, sub, mul, div, mod;

  sum = n1 + n2;
  sub = n1 - n2;
  mul = n1 * n2;
  div = n1 / n2;
  mod = n1 % n2;

  print("SUM = ${sum}");
  print("SUBTRACT = ${sub}");
  print("MULTIPLY = ${mul}");
  print("DIVIDE = ${div}");
  print("MOD = ${mod}");
}
